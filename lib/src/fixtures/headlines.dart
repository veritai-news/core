        eventCountry: country,
        topic: topic,
        createdAt: referenceTime.subtract(Duration(minutes: i * 15)),
        updatedAt: referenceTime.subtract(Duration(minutes: i * 15)),
        status: ContentStatus.active,
      ),
    );
  }
  return headlines;
}

