      Headline(
        id: id,
        isBreaking: i % 10 == 3, // Make some headlines breaking
        title: title,
        url: 'https://example.com/news/${id.substring(0, 8)}',
        imageUrl: 'https://picsum.photos/seed/$id/800/600',
